Cypress.Commands.add('getBySel', (selector, ...args) => cy.get(`[data-cy=${selector}]`, ...args));

Cypress.Commands.add('inputForm', (name) => {
  cy.getBySel('name')
    .type(name)
    .should('have.value', name);
  cy.getBySel('submit')
    .click({ force: true });
});
